
package Logica;

//Clase Suma.
public class vsuma {
    int resultado;
    //metodo sumar de dos numeros.
    public int vsuma (int valor1, int valor2){
        resultado=valor1+valor2;
        return resultado;
        
    }
//    metodo restar de dos numeros.
    public int restar (int valor1, int valor2){
        resultado=valor1-valor2;
        return resultado;
    }
    
}
